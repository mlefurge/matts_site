$(document).ready(function(){

  $('#contact_me').on('click', function(){
    $('.generic_info').slideToggle();
    $('#contact_me').fadeToggle();
    $('.contact_form_container').slideToggle();
  });

  $('#cancel').on('click', function(event){
    event.preventDefault();
    $('.contact_form_container').slideToggle();
    $('#contact_me').fadeToggle();
    $('.generic_info').slideToggle();
  })


})
